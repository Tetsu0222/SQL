//日時ファンクション


//日時＋数字
SELECT TO_DATE( '2021/01/01 00:00:00' ) + 1
FROM dual;

→2021/01/02 00:00:00


//時間単位
SELECT TO_DATE( '2021/01/01 00:00:00' ) + 1/24
FROM dual;

→2021/01/01 01:00:00


//分単位
SELECT TO_DATE( '2021/01/01 00:00:00' ) + 1/1440
FROM dual;

→2021/01/01 00:01:00



//日時-日時
SELECT TO_DATE( '2021/01/02 00:00:00' ) - TO_DATE( '2021/01/01 00:00:00' )
FROM dual;

→1


//日時+日時は実行不可